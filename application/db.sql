CREATE TABLE users (
    id SERIAL PRIMARY KEY,
    username VARCHAR(30) NOT NULL,
    email_address VARCHAR(50) NOT NULL,
    password VARCHAR(60) NOT NULL,
    f_name VARCHAR(30),
    lName VARCHAR(30),
    dob DATE,
    sex SMALLINT,
    weight SMALLINT,
    height SMALLINT,
    sysBP SMALLINT,
    diaBP SMALLINT,
    glucose SMALLINT,
    totChol SMALLINT,
    cigsPerDay SMALLINT,
    prevalentHyp SMALLINT,
    BPMeds SMALLINT,
    diabetes SMALLINT,
    education SMALLINT,
    currentSmoker SMALLINT,
    heartRate SMALLINT,
    prevalentStroke SMALLINT
);
